export const colorScales: ColorScale[] = palette.map(({
  name,
  hex,
  colors,
}) => ({
  hex,
  name,
  scale: colors.reduce<ColorScaleMap>((map, _color, index) => {
    const color = chroma(_color)
    const weight = indexToWeight(COLORS_COUNT, index, WEIGHT_MAXIMUM, WEIGHT_MINIMUM)

    return {
      [weight]: map[weight] = `rgb(${color
        .rgb()
        .join(',')
      })`,
      ...map
    }
  }, {})
}))
